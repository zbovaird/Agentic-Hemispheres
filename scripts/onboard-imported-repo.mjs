#!/usr/bin/env node
/**
 * First-pass repo structure doc for imported repositories.
 * Usage: node scripts/onboard-imported-repo.mjs [--dry-run]
 */

import { readdirSync, writeFileSync, readFileSync, existsSync, mkdirSync } from "node:fs";
import { join, dirname } from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const ROOT = join(__dirname, "..");
const OUT = join(ROOT, "docs/repository-structure.md");

const dryRun = process.argv.includes("--dry-run");

const SKIP = new Set([
  ".git",
  "node_modules",
  ".cursor",
]);

function listTopLevel() {
  return readdirSync(ROOT, { withFileTypes: true })
    .filter((d) => d.isDirectory() && !SKIP.has(d.name))
    .map((d) => d.name)
    .sort();
}

function loadWorkspacePending() {
  const p = join(ROOT, ".cursor/plans/workspace_state.json");
  if (!existsSync(p)) return [];
  try {
    const j = JSON.parse(readFileSync(p, "utf-8"));
    return Array.isArray(j.summary_debt_pending) ? j.summary_debt_pending : [];
  } catch {
    return [];
  }
}

function writePendingNote(dirs) {
  const p = join(ROOT, ".cursor/plans/workspace_state.json");
  if (!existsSync(p) || dryRun) return;
  const raw = JSON.parse(readFileSync(p, "utf-8"));
  const pending = new Set(Array.isArray(raw.summary_debt_pending) ? raw.summary_debt_pending : []);
  pending.add(`Fill domain ownership in docs/repository-structure.md for: ${dirs.join(", ")}`);
  raw.summary_debt_pending = [...pending];
  mkdirSync(dirname(p), { recursive: true });
  writeFileSync(p, JSON.stringify(raw, null, 2) + "\n", "utf-8");
}

const dirs = listTopLevel();
const body = `# Repository structure

This file was generated by \`npm run onboard:imported-repo\`. Replace placeholders with domain ownership and invariants.

## Top-level directories

${dirs.map((d) => `- \`${d}/\` — _describe purpose_`).join("\n")}

## Notes

- See [docs/MAP.md](MAP.md) and [docs/summary-policy.md](summary-policy.md) for navigation and summary rules.
- Prefer folder READMEs for heavy subtrees when you add them.

`;

if (dryRun) {
  console.log(body);
  process.exit(0);
}

mkdirSync(dirname(OUT), { recursive: true });
writeFileSync(OUT, body, "utf-8");
console.log(`onboard-imported-repo: wrote ${OUT}`);

writePendingNote(dirs);
console.log("onboard-imported-repo: done (summary_debt_pending updated if workspace_state exists)");
